public class Atribuição {
    public static void main(String[] args) {
        // Declaração de variáveis:
        int a;
        int b = 100;

        // Atribuição de variável =:
        a = b;

        System.out.println(a);

        // Somando o conteúdo da variável a com a variável b:
        a = a + b;

        System.out.println(a);

        // Somando o conteúdo da variável a com a variável b:
        a += b;

        System.out.println(a);
    }
}
